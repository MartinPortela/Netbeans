
package caminoAleatorio;


public class Nodo
{
    private int numNodo;
    private int numSig=-1;
    
    /******************************************************************************************************************************************/
    public int getNumNodo() 
    {
        return numNodo;
    }
    /******************************************************************************************************************************************/
    public int getNumSig()
    {
        return numSig;
    }
    /******************************************************************************************************************************************/
    public Nodo(int numNodo) 
    {
        this.numNodo = numNodo;
    }
    /******************************************************************************************************************************************/
    public void setNumSig(int numSig) 
    {
        this.numSig = numSig;
    }
    /******************************************************************************************************************************************/
    @Override
    public String toString() {
        return "Nodo{" + "numNodo=" + numNodo + ", numSig=" + numSig + '}';
    }
    
}
