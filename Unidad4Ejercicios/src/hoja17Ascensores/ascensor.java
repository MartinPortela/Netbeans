

package hoja17Ascensores;


public class ascensor 
{
    private int planta;
    private int viajes=0;
    private int ID;

    public ascensor(int planta, int ID) {
        this.planta = planta;
        this.ID = ID;
    }
/******************************************************************************************************************************************/
    public int getPlanta() {
        return planta;
    }
/******************************************************************************************************************************************/
    public int getViajes() {
        return viajes;
    }
/******************************************************************************************************************************************/
    public int getID() {
        return ID;
    }
    
/******************************************************************************************************************************************/
    public void setPlanta(int planta) {
        this.planta = planta;
    }
/******************************************************************************************************************************************/
    public void setID(int ID) {
        this.ID = ID;
    }
/******************************************************************************************************************************************/  
    @Override
    public String toString() {
        return "ascensor{" + "planta=" + planta + ", viajes=" + viajes + ", ID=" + ID + '}';
    }
         
}
